# dp[1] = 1 (1)
# dp[2] = 1 (10)
# dp[3] = 2 (101 100)
# dp[4] = 3 (1010 1001 1000)
# dp[5] = 5 (10101 10100 10010 10001 10000)
# dp[n][0] = dp[n-2][1]
# dp[n][1] = dp[n-1][0]
# n-2번째를 01로 채우느냐 n-1번째를 0으로 채우느냐 (=> 2차원리스트로 잘못생각, 같은 결론이 도출되긴 함)

# => dp[i] = dp[i-1] + dp[i-2]
n = int(input())

dp = [0] * 91
dp[1] = 1
dp[2] = 1
dp[3] = 2

for i in range(4, n+1):
    dp[i] = dp[i-1] + dp[i-2]
    
print(dp[n])